//From https://esolangs.org/wiki/Brainfuck_bitwidth_conversions

char * expand8to16NoCopy(char b){
	switch(b){
		case '>':
			return ">>>>";
		break;
		case '<':
			return "<<<<";
		break;
		case '+':
			return ">+<+[>-]>[->>+<]<<";
		break;
		case '-':
			return ">+<[>-]>[->>-<]<<-";
		break;
		case '[':
			return ">+<[>-]>[->+>[<-]<[<]>[-<+>]]<-[+<";
		break;
		case ']':
			return ">+<[>-]>[->+>[<-]<[<]>[-<+>]]<-]<";
		break;
	}
	return "";
}
char * expand8to16(char b){
	switch(b){
		case '>':
			return ">>>";
		break;
		case '<':
			return "<<<";
		break;
		case '+':
			return "+>+[<->[->>+<<]]>>[-<<+>>]<<<[->>+<<]";
		break;
		case '-':
			return "+>[<->[->>+<<]]>>[-<<+>>]<<<[->>-<<]>-<";
		break;
		case '[':
			return ">[<+>[->>+<<]]>>[-<<+>>]<[<<+>>[->+<]]>[-<+>]<<<[[-]";
		break;
		case ']':
			return ">[<+>[->>+<<]]>>[-<<+>>]<[<<+>>[->+<]]>[-<+>]<<<]";
		break;
	}
	return "";
}
char * expand8to32(char b){
	switch(b){
		case '>':
			return ">>>>>";
		break;
		case '<':
			return "<<<<<";
		break;
		case '+':
			return "+>+[<->[->>>>+<<<<]]>>>>[-<<<<+>>>>]<<<<<[>>+[<<->>[->>>+<<<]]>>>[-<<<+>>>]<<<<<[>>>+[<<<->>>[->>+<<]]>>[-<<+>>]<<<<<[->>>>+<<<<]]]";
		break;
		case '-':
			return "+>[<->[->>>>+<<<<]]>>>>[-<<<<+>>>>]<<<<<[>>[<<->>[->>>+<<<]]>>>[-<<<+>>>]<<<<<[>>>[<<<->>>[->>+<<]]>>[-<<+>>]<<<<<[->>>>-<<<<]>>>-<<<]>>-<<]>-<";
		break;
		case '[':
			return ">[<+>[->>>>+<<<<]]>>>>[-<<<<+>>>>]<<<[<<+>>[->>>+<<<]]>>>[-<<<+>>>]<<[<<<+>>>[->>+<<]]>>[-<<+>>]<[<<<<+>>>>[->+<]]>[-<+>]<<<<<[[-]";
		break;
		case ']':
			return ">[<+>[->>>>+<<<<]]>>>>[-<<<<+>>>>]<<<[<<+>>[->>>+<<<]]>>>[-<<<+>>>]<<[<<<+>>>[->>+<<]]>>[-<<+>>]<[<<<<+>>>>[->+<]]>[-<+>]<<<<<]";
		break;
	}
	return "";
}